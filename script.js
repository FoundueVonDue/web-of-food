function click(d) {
    if (d.children) {
        d._children = d.children;

        d.children = null;
        d.expanded = false;  // Track if node is expanded or not
    } else {
        d.children = d._children;
        d._children = null;
        d.expanded = true;  // Track if node is expanded or not
    }
    update(d);
}

function collapse(d) {
    if (d.children) {
        d._children = d.children;
        d._children.forEach(collapse);
        d.children = null;
        d.expanded = false;
    }
}


